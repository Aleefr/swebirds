<p style="font-family: Monaco; width: 313px;text-align: center;" >When sculptors are forced into a small space, sculptures become small. Lemma.net is a project about sculptures without spaces.
 
 When a sculpture occupies a public space, it claims the mundane. It might start as an interruption of daily movement, but soon becomes incorporated into the everyday routine. Saddam Hussein was a sculpture, a statue, he fell, he looked like a chocolate bunny. For the Power, a statue is not perfect because of the physical property of falling. A crypto-statue might be fool-proof.
  
 I haven’t moved in public for 4 years (and my life is a projection). If they want me, they need to come get me.
  
 How is the service announcement not a sculpture? How can I, as a sculptor, compete if I work with marble? And if my room is still a physical space? Lemma.net seeks to understand sculpture in an expanded sphere of viewing and interaction. It tries to follow the mundane into digital existence. A videogame without a narrative, without an end becomes a sculptural form. An intrusive virus becomes a vantage point, a raised statue looking over the city.
  
  
 With being Russian/Greek and feeling nomadic
  
 To assume a geometric space in one's imaginative montages, one has the leisure of timelessness, while at the same time one is forced in movement of ever declining possibilities to select, and every selected expressive attempt follows the expresser into a gradual collapse. One is forced into a geometry of pressure, demanding victory in every breath. Then Lemma.net enters a digital geometry where it can defy its negation.
 
 A falling apart garden, tempered in commotion to harvest ripe negation.
 Where the visitor is infected by the singing of Swedish birds, an event that disperses an expressive spillage from user to user, a moisturized density of a distinct declining trajectory of a sculpture to invade our assumption of space.
 
 A fountain of sculptures, shooting variations, falling and breaking.
